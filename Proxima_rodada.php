                <h2> Por vaga na final da Copa do Brasil, Santos e São Paulo duelam na Vila </h2>
                <h4 id="texto1">Em busca de uma vaga na decisão da Copa do Brasil, Santos e São Paulo se enfrentam novamente nesta quarta-feira, às 22h (de Brasília), na Vila Belmiro, no jogo de volta da semifinal da competição. Como venceu a partida de ida por 3 a 1, no Morumbi, o Peixe pode até perder por 2 a 0 que avança.
    Para chegar à final, o Tricolor precisa fazer 3 a 0 ou ganhar por dois gols de diferença, desde que faça quatro ou mais (4 a 2, 5 a 3 ou 6 a 4, por exemplo). O duelo vai para os pênaltis em caso de vitória por 3 a 1 dos visitantes.
    Luiz Flavio de Oliveira apita o confronto decisivo, auxiliado por Emerson Augusto de Carvalho e Marcelo Carvalho Van Gasse. A TV Globo transmite o jogo para SP, PR, GO, TO, MS, MT, BA, CE e PA (menos Santarém), com Cléber Machado, Casagrande e Leonardo Gaciba. SporTV 2 e PFCI (com Milton Leite, Belletti e Lédio Carmona) também transmitem a partida. O GloboEsporte.com acompanha todos os lances em Tempo Real, com vídeos exclusivos.
                    </br>
                    </br>
                <p>Santos: o técnico Dorival Júnior fechou os dois treinos do Peixe antes do clássico contra o São Paulo, mas não deve promover surpresas. Thiago Maia, com um edema na coxa direita, é dúvida. O Alvinegro deve entrar em campo nesta quarta com: Vanderlei; Daniel Guedes, David Braz, Werley (Gustavo Henrique) e Zeca; Thiago Maia (Paulo Ricardo), Renato e Lucas Lima; Gabriel, Ricardo Oliveira e Marquinhos Gabriel.
    São Paulo: o treinador Doriva tem dúvidas para montar o Tricolor no clássico. O time deve entrar em campo com: Rogério Ceni; Bruno, Lucão, Luiz Eduardo e Matheus Reis; Wesley (Hudson ou João Paulo), Rodrigo Caio, Michel Bastos, Ganso e Pato; Luis Fabiano (Alan Kardec).</p></h4>
