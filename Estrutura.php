           <h1 id="titulo">Arena Corinthians</h1>
                <img id="img2" width="10" height="5" src="imgsp/morumbi.jpeg"/>    
                <h3 id="artigo">   Arena Corinthians,popularmente conhecida como Itaquerão, é um estádio de futebol localizado no distrito de Itaquera, na Zona Leste do município de São Paulo, Brasil. De propriedade do Sport Club Corinthians Paulista, sua capacidade oficial é para 47 605 lugares, sendo o 12º maior estádio do Brasil. O Ministério do Esporte lançou, o Sistema Brasileiro de Classificação de Estádios (Sisbrace), que propõe a melhoria em conforto, segurança, acessibilidade e condições sanitárias e de higiene dos estádios do país, tendo a Arena Corinthians, recebendo avaliação máxima com cinco bolas. Além de ganhar também o prêmio de melhor projeto de arquitetura do país no ano de 2011.A arena foi inaugurada oficialmente em 18 de maio de 2014 em uma partida entre a equipe do Corinthians e o Figueirense, perdendo pelo placar de 1 a 0, na Série A do Brasileiro daquela temporada. Poucas semanas depois de sua inauguração oficial, sediou a cerimônia de abertura da Copa do Mundo FIFA de 2014, que incluiu a partida entre Brasil e Croácia, e outros cinco duelos do torneio, entre os quais uma semifinal. Devido a exigência da FIFA de pelo menos 60 mil lugares para o jogo inaugural da Copa do Mundo, foram incluídos mais 19 800 assentos temporários ao estádio exclusivamente para atender a exigência.Com o fim do Mundial de 2014, o Corinthians determinou a retirada das estruturas temporárias.Construído pela Odebrecht entre 2011 e 2014, tinha previsão de custo inicial em 820 milhões de reais, mas seu preço final atingiu quase 1,2 bilhão de reais, incluindo uma isenção tributária de 420 milhões de reais por parte da prefeitura do município de São Paulo. Além dos vastos recursos financeiros utilizados em sua construção, a arena também recebeu críticas pelas circunstâncias em que foi escolhida como o estádio oficial da Copa do Mundo e pelas condições de trabalho dos operários que participaram da obra. A Arena Corinthians atingiu a marca de 2 milhões de torcedores(as) no seu 61º jogo, tendo média de 32 801 pagantes por partida e arrecadou o montante superior a R$ 124 milhões.</h3>
