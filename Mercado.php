                <h1 id="artigo">Marlone na mira do Atlético-PR</h1>
                <img id="img2" src="imgsp/iagospfc.jpeg"/>
                <h4 id="texto2">Sem atuar desde o dia 4 de junho e com apenas 14 partidas disputadas pelo Corinthians em sete meses, o meia Marlone não foi relacionado para o confronto contra o Figueirense, neste sábado, pela 16ª rodada do Campeonato Brasileiro. Apesar de o técnico Cristóvão Borges ter dito que ele terá oportunidades no Timão, o camisa 8 pode estar de saída do clube nos próximos dias. Depois de Sport e Ponte Preta, como informou o L! há duas semanas, novos clubes entraram na disputa pelo jogador, mas o Atlético-PR hoje é favorito pela contratação por empréstimo até o fim da temporada. Há possibilidade de o Corinthians anunciar a saída de seu jogador ainda nesta sexta-feira. 

A falta de chances a Marlone é um fato que tem chamado atenção da Fiel torcida. Com Tite, o interino Fabio Carille e mesmo Cristóvão Borges em seus seis primeiros jogos, o camisa 8 é opção no banco de reservas, mas em poucas vezes acaba acionado. A última vez, por exemplo, foi no dia 4 de junho, na vitória por 2 a 1 sobre o Coritiba, na Arena. Marlone até começou jogando naquele dia, mas saiu logo no intervalo. Após o jogo, evitou desabafos mais intensos, mas marcou território no elenco. E mesmo assim já não entra em campo há nove partidas - dez contra o Figueirense. 

Marlone se destacou no Brasileirão do ano passado com a camisa do Sport, e o clube voltou a procurá-lo neste ano, mas na ocasião não houve acerto. Além do clube nordestino, a Ponte Preta do técnico Eduardo Baptista e o Internacional, agora dirigido por Paulo Roberto Falcão, também realizaram contato, mas nada feito. Por último, o Atlético-PR entrou na disputa por indicação do técnico Paulo Autuori, que o dirigiu no Vasco, e os representantes de Marlone se animaram com a oportunidade.

Apesar do Timão querer demover Marlone da ideia de atuar emprestado a outro clube, o jogador está incomodado com a falta de oportunidades no Parque São Jorge e autorizou a negociação. As duas diretorias estão conversando, e por isso o camisa 8 não foi relacionado contra o Figueirense - consultado, o Corinthians ainda não se posicionou a respeito do assunto. Cristóvão, porém, explicou as razões pelas quais não oportuniza o jogador.</h4>
